function [Ln,V] = get_mesh_info(P1,P2,P3,P4)
% 四面体单元各面2倍面积乘对应单位外法向量：Si*ni

Ln(:,:,1)=[-((P3(2,:)-P2(2,:)).*(P4(3,:)-P2(3,:))-(P3(3,:)-P2(3,:)).*(P4(2,:)-P2(2,:)));
                 ((P3(1,:)-P2(1,:)).*(P4(3,:)-P2(3,:))-(P3(3,:)-P2(3,:)).*(P4(1,:)-P2(1,:)));
                -((P3(1,:)-P2(1,:)).*(P4(2,:)-P2(2,:))-(P3(2,:)-P2(2,:)).*(P4(1,:)-P2(1,:)))];
Ln(:,:,2)=-[-((P3(2,:)-P1(2,:)).*(P4(3,:)-P1(3,:))-(P3(3,:)-P1(3,:)).*(P4(2,:)-P1(2,:)));
                  ((P3(1,:)-P1(1,:)).*(P4(3,:)-P1(3,:))-(P3(3,:)-P1(3,:)).*(P4(1,:)-P1(1,:)));
                 -((P3(1,:)-P1(1,:)).*(P4(2,:)-P1(2,:))-(P3(2,:)-P1(2,:)).*(P4(1,:)-P1(1,:)))];
Ln(:,:,3)=[-((P2(2,:)-P1(2,:)).*(P4(3,:)-P1(3,:))-(P2(3,:)-P1(3,:)).*(P4(2,:)-P1(2,:)));
                 ((P2(1,:)-P1(1,:)).*(P4(3,:)-P1(3,:))-(P2(3,:)-P1(3,:)).*(P4(1,:)-P1(1,:)));
                -((P2(1,:)-P1(1,:)).*(P4(2,:)-P1(2,:))-(P2(2,:)-P1(2,:)).*(P4(1,:)-P1(1,:)))];
Ln(:,:,4)=-[-((P2(2,:)-P1(2,:)).*(P3(3,:)-P1(3,:))-(P2(3,:)-P1(3,:)).*(P3(2,:)-P1(2,:)));
                  ((P2(1,:)-P1(1,:)).*(P3(3,:)-P1(3,:))-(P2(3,:)-P1(3,:)).*(P3(1,:)-P1(1,:)));
                 -((P2(1,:)-P1(1,:)).*(P3(2,:)-P1(2,:))-(P2(2,:)-P1(2,:)).*(P3(1,:)-P1(1,:)))];  %  四面体四个面的单位外法向量*两倍对应面面积

V=abs((P1(1,:)-P4(1,:)).*((P2(2,:)-P4(2,:)).*(P3(3,:)-P4(3,:))-(P2(3,:)-P4(3,:)).*(P3(2,:)-P4(2,:)))-...
      (P2(1,:)-P4(1,:)).*((P1(2,:)-P4(2,:)).*(P3(3,:)-P4(3,:))-(P1(3,:)-P4(3,:)).*(P3(2,:)-P4(2,:)))+...
      (P3(1,:)-P4(1,:)).*((P1(2,:)-P4(2,:)).*(P2(3,:)-P4(3,:))-(P1(3,:)-P4(3,:)).*(P2(2,:)-P4(2,:)))); % 四面体体积的六倍 6V

end